// 簡單的翻譯函數
export function t(key: string, locale: Locale = defaultLocale, params?: Record<string, string>, fallback?: string): string {
  try {
    const keys = key.split('.')
    let value: any = translations[locale]
    
    for (const k of keys) {
      if (value && typeof value === 'object') {
        value = value[k]
      } else {
        value = undefined
        break
      }
    }
    
    if (typeof value !== 'string') {
      // 如果找不到翻譯，嘗試使用預設語言
      if (locale !== defaultLocale) {
        return t(key, defaultLocale, params, fallback)
      }
      // 如果有提供 fallback，使用 fallback，否則返回 key
      return fallback || key
    }
    
    // 簡單的參數替換
    if (params) {
      return value.replace(/\{\{(\w+)\}\}/g, (match, param) => {
        return params[param] || match
      })
    }
    
    return value
  } catch (error) {
    console.error(`Translation error for key: ${key}`, error)
    return fallback || key
  }
}